package org.example;

public class Square {
    int squareX;
    int squareY;

    public Square(int squareX, int squareY) {
        this.squareX = squareX;
        this.squareY = squareY;
    }
    public int getSquareX() {
        return this.squareX;
    }

    public int setSquareY(){
        return this.squareY;
    }

}
